"""
趣味用户名生成器
格式：{形容词}{的}{英文名}  例如：活泼的汤姆、神秘的爱丽丝
"""

import random

_ADJECTIVES = [
    "活泼", "安静", "勇敢", "聪明", "温柔", "阳光", "神秘", "可爱",
    "机智", "淡定", "开朗", "认真", "风趣", "沉稳", "热情", "冷静",
    "灵动", "踏实", "幽默", "专注", "睿智", "豁达", "坚韧", "洒脱",
]

_NAMES = [
    "汤姆", "爱丽丝", "鲍勃", "艾玛", "杰克", "奥利维亚", "莱昂",
    "索菲亚", "卢卡斯", "伊莎贝拉", "亨利", "米娅", "奥斯卡", "阿梅莉亚",
    "芬恩", "夏洛特", "诺亚", "艾拉", "利亚姆", "阿娃", "以利亚",
    "斯嘉丽", "詹姆斯", "格蕾丝", "本杰明", "克洛伊", "塞巴斯蒂安",
    "佐伊", "马修", "莉莉", "亚历山大", "汉娜", "丹尼尔", "娜塔莉",
]


def generate_username() -> str:
    adj = random.choice(_ADJECTIVES)
    name = random.choice(_NAMES)
    return f"{adj}的{name}"